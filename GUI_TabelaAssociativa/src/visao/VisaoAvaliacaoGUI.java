    private void comboAlunoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboAlunoItemStateChanged
        // TODO add your handling code here:
        String alunoSelecionado = (String) comboAluno.getSelectedItem();
        //JOptionPane.showMessageDialog(this, "Aluno selecionado:" + alunoSelecionado);
        ResultSet avaliacoes = DadosHabilidade.obtemAvaliacoesAluno(alunoSelecionado);
        tabelaHabilidades.setModel(DBUtils.resultSetToTableModel(avaliacoes));
        comboHabilidade.setModel(new HabilidadeComboModel(alunoSelecionado));
    }//GEN-LAST:event_comboAlunoItemStateChanged

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if (comboAluno.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(this, "Selecione um aluno.");
            return;
        }
        if (tabelaHabilidades.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma habilidade na tabela.");
            return;           
        }    
        String ra = DadosHabilidade.obtemRA((String)comboAluno.getSelectedItem());
        int id = Integer.valueOf(tabelaHabilidades.getModel().getValueAt(tabelaHabilidades.getSelectedRow(),0).toString());
        //JOptionPane.showMessageDialog(this, "Dados :"+ra+" "+id);
        DadosHabilidade.excluirAvaliacao(ra, id);
        comboAlunoItemStateChanged(null);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if (comboAluno.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(this, "Selecione um aluno.");
            return;
        }
        if (tabelaHabilidades.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma habilidade na tabela.");
            return;           
        }    
        String nomeAluno = (String)comboAluno.getSelectedItem();
        String descHabilidade = tabelaHabilidades.getModel().getValueAt(tabelaHabilidades.getSelectedRow(),1).toString();
        String valorAvaliacao = tabelaHabilidades.getModel().getValueAt(tabelaHabilidades.getSelectedRow(),2).toString();
        btnExcluirActionPerformed(null);
        txtAvaliacao.setText(valorAvaliacao);
        comboAluno.setSelectedItem(nomeAluno);
        comboHabilidade.setSelectedItem(descHabilidade);
    }//GEN-LAST:event_btnEditarActionPerformed

    
