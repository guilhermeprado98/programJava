    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (comboAluno.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(this, "Selecione um aluno.");
            return;
        }
        if (comboHabilidade.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(this, "Selecione uma habilidade.");
            return;           
        }
        if ( txtAvaliacao.getText().equals("") ) {
            JOptionPane.showMessageDialog(this, "Digite uma nota para a avaliação.");
            return;           
        }
        
        String ra = DadosHabilidade.obtemRA((String)comboAluno.getSelectedItem());
        int id = DadosHabilidade.obtemID((String)comboHabilidade.getSelectedItem());
        int avaliacao = Integer.valueOf(txtAvaliacao.getText());
        //JOptionPane.showMessageDialog(this, "Dados :"+ra+" "+id);
        
        DadosHabilidade.incluirAvaliacao(ra, id, avaliacao);
        comboAlunoItemStateChanged(null);
        txtAvaliacao.setText("");
    }//GEN-LAST:event_btnSalvarActionPerformed

